
# assignment operators - operatory przypisania

a = 12
a += 1       # a = a + 1   12+1=13
a -= 1       # a = a - 1   13-1=12
a *= 2       # 24
a /= 3      # 8
a %= 3      # 2 - reszta z dzielenia
print(a)
a **= 4     # 16
print(a)
a //= 5     # 3 - zwraca dzielenie bez reszty (16/5=3.2)
print(a)


