# zmienna None pusta zmienna
currentTaskNumber = 10              # zmienna ma wartość
print(currentTaskNumber)
print(type(currentTaskNumber))

currentTaskNumber = None            # zmienna nie ma wartości (None) - korzystamy, kiedy chcemy np czasowo 
print(currentTaskNumber)            # zmienna nie posiadała wartości <class 'NoneType'>
print(type(currentTaskNumber))

currentTaskNumber = 14              # później możemy nadać ponownie wartość zmiennej
print(currentTaskNumber)
print(type(currentTaskNumber))

