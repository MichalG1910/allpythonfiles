
# comparison operators - operatory porównania

result = 12 == 8 # czy 12 jest równe 8 - wynik: False
print(result)

print( 12 != 4) # czy 12 nie jest równe 4 - wynik: True
print( 12 != 12) # False

print( 8 > 1 ) # True
print( 8 > 10 ) # False
print( 5 >= 3) # True      wieksze/mniejsze lub równe
print( 5 >= 5) # True

