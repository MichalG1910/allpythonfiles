
# membership operators - operatory przynależności in, not in

data = [0,1,2,3,4,5]

print( -1 in data) # Fals - operator in sprawdza,czy wartość jest np. w zmiennej, krotce liście itd.
print( 3 in data) # True
print( "Ola" in ("Ola", "Ania")) # True

print( 10 not in data) # True - operator not in sprawdza, czy wartość nie jest np. w zmiennej, krotce liście itd.
print( 2 not in data) # False